package com.mjl.model;

public class Sign {
    private int formId;
    private String emplName;
    private int emplId;
    private String time;
    private String login;
    private String loginState;
    private String signOut;
    private String signOutState;
    private String trueTime;

    public int getFormId() {
        return formId;
    }

    public void setFormId(int formId) {
        this.formId = formId;
    }

    public String getEmplName() {
        return emplName;
    }

    public void setEmplName(String emplName) {
        this.emplName = emplName;
    }

    public int getEmplId() {
        return emplId;
    }

    public void setEmplId(int emplId) {
        this.emplId = emplId;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getLogin() {
        return login;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public String getLoginState() {
        return loginState;
    }

    public void setLoginState(String loginState) {
        this.loginState = loginState;
    }

    public String getSignOut() {
        return signOut;
    }

    public void setSignOut(String signOut) {
        this.signOut = signOut;
    }

    public String getSignOutState() {
        return signOutState;
    }

    public void setSignOutState(String signOutState) {
        this.signOutState = signOutState;
    }

    public String getTrueTime() {
        return trueTime;
    }

    public void setTrueTime(String trueTime) {
        this.trueTime = trueTime;
    }

}
